import cv2
image=cv2.imread('image.png')
image=cv2.cvtColor(image,cv2.COLOR_BGR2GRAY) #convert to gray
#image = cv2.resize(image, (350,350), interpolation = cv2.INTER_AREA)
blur=cv2.GaussianBlur(image, (15,15), 1)
blur1=cv2.GaussianBlur(image, (15,15), 5)
blur2=cv2.GaussianBlur(image, (15,15), 10)
blur3=cv2.GaussianBlur(image, (15,15), 20)
cv2.imshow('original',image)
cv2.imshow('blur1',blur)
cv2.imshow('blur5',blur1)
cv2.imshow('blur10',blur2)
cv2.imshow('blur20',blur3)
cv2.waitKey(0)
cv2.destroyAllWindows()